#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	unsigned int	len;
	unsigned int	i;
	char			*res;

	if (!s)
		return (NULL);
	len = ft_strlen(s);
	res = (char *)malloc((sizeof(char) * len) + 1);
	if (!res)
		return (NULL);
	i = 0;
	while (i < len)
	{
		res[i] = (*f)(i, s[i]);
		i++;
	}
	res[len] = '\0';
	return (res);
}
